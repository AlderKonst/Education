import random

# А не слишком ли сложновато задание для light, а?

peoples = ['В.В. Путина', 'E. Mask', 'А.С. Пушкина', 'И.В. Грозного', 'Пётра I', 'Сталина И.В.', 'Ленина В.И.', 'Ельцина Б.Н.', 'Горбачева М.С.', 'Хрущева Н.С.']
